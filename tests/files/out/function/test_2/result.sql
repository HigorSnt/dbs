CREATE
OR REPLACE FUNCTION isPalindrome (text VARCHAR2) RETURN VARCHAR2 IS
BEGIN
SELECT
  REVERSE(text) INTO reversedText
FROM
  DUAL;

-- Using UPPER to ignore case sensitivity.
IF UPPER(text) = UPPER(reversedText) THEN RETURN('true');

END IF;

RETURN ('false');

END isPalindrome;

CREATE
OR REPLACE FUNCTION MATH_FUNCTIONS.fdiv (n1 IN NUMBER, n2 IN NUMBER) RETURN NUMBER IS n3 NUMBER(8);

BEGIN
  n3 := n1 / n2;

RETURN n3;

END MATH_FUNCTIONS.fdiv;

